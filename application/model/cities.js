/**
 * @param {number|Array.<number>} countryId
 * @param {string=} opt_query
 * @return {!Array.<Cities.Item>}
 */
model.getAllByCountry = function(countryId, opt_query) {
  var regionIds = regionsModel.getAllByCountry(countryId).map(function(region) {
    return region.id;
  });

  return model.getAllByRegion(regionIds, opt_query);
};

/**
 * @param {number|Array.<number>} regionId
 * @param {string=} opt_query
 * @return {!Array.<Cities.Item>}
 */
model.getAllByRegion = function(regionId, opt_query) {
  var regionIds = typeof regionId == 'number' ? [regionId] : regionId;

  return model.getAll(opt_query).filter(function(city) {
    return -1 < regionIds.indexOf(city.region_id);
  });
};

var getAll = model.getAll;

/**
 * @param {string=} opt_query
 * @return {!Array.<Cities.Item>}
 */
model.getAll = function(opt_query) {
  var cities = getAll.call(model);

  return cities.filter(function(city) {
    if (opt_query) {
      var query = opt_query.toUpperCase();

      return 0 == city.name.toUpperCase().indexOf(query) ||
        0 == city.name_en.toUpperCase().indexOf(query);
    } else {
      return true;
    }
  });
};
