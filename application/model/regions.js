/**
 * @param {number|Array.<number>} countryId
 * @param {string=} opt_query
 * @return {!Array.<Regions.Item>}
 */
model.getAllByCountry = function(countryId, opt_query) {
  var countryIds = typeof countryId == 'number' ? [countryId] : countryId;

  return model.getAll(opt_query).filter(function(region) {
    return -1 < countryIds.indexOf(region.country_id);
  });
};

var getAll = model.getAll;

/**
 * @param {string=} opt_query
 * @return {!Array.<Regions.Item>}
 */
model.getAll = function(opt_query) {
  var regions = getAll.call(model);

  return regions.filter(function(region) {
    if (opt_query) {
      var query = opt_query.toUpperCase();

      return 0 == region.name.toUpperCase().indexOf(query);
    } else {
      return true;
    }
  });
};
